package StreamAPI;

import java.util.Date;

public class Book {
    private String name;
    private int page;
    private String author;
    private Date date;

    public Book(String name, int page, String author, Date date) {
        this.name = name;
        this.page = page;
        this.author = author;
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
}
